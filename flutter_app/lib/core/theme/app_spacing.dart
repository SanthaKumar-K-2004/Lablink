class AppSpacing {
  // Base unit: 8px

  // Spacing scale
  static const double zero = 0;
  static const double xs = 4; // tight spacing
  static const double sm = 8; // small gaps
  static const double md = 12; // default
  static const double lg = 16; // comfortable
  static const double xl = 24; // generous
  static const double xxl = 32; // large gaps
  static const double xxxl = 48; // extra large
  static const double xxxxl = 64; // section spacing

  // Common padding combinations
  static const double paddingXSmall = xs;
  static const double paddingSmall = sm;
  static const double paddingMedium = md;
  static const double paddingLarge = lg;
  static const double paddingExtraLarge = xl;
  static const double paddingHuge = xxl;

  // Common margins
  static const double marginXSmall = xs;
  static const double marginSmall = sm;
  static const double marginMedium = md;
  static const double marginLarge = lg;
  static const double marginExtraLarge = xl;
  static const double marginHuge = xxl;

  // Button padding
  static const double buttonPaddingXSmall = 10;
  static const double buttonPaddingSmall = 12;
  static const double buttonPaddingMedium = 24;
  static const double buttonPaddingLarge = 32;

  // Horizontal spacing
  static const double gapXSmall = xs;
  static const double gapSmall = sm;
  static const double gapMedium = md;
  static const double gapLarge = lg;
  static const double gapExtraLarge = xl;
  static const double gapHuge = xxl;

  // Common section dividers
  static const double sectionGap = xxxxl; // 64px
  static const double cardGap = xl; // 24px
}
